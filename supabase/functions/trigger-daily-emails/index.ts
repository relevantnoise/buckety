import { serve } from "https://deno.land/std@0.190.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2.55.0";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

const handler = async (req: Request): Promise<Response> => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    console.log('[TRIGGER-EMAILS] Manually triggering daily emails');

    // Initialize Supabase client
    const supabase = createClient(
      Deno.env.get('SUPABASE_URL') ?? '',
      Deno.env.get('SUPABASE_SERVICE_ROLE_KEY') ?? ''
    );

    // Call the send-daily-emails function directly
    const result = await supabase.functions.invoke('send-daily-emails', {
      body: { forceDelivery: true } // Override time check
    });

    if (result.error) {
      throw new Error(`Failed to trigger emails: ${result.error.message}`);
    }

    console.log('[TRIGGER-EMAILS] Daily emails triggered successfully:', result.data);

    return new Response(
      JSON.stringify({
        success: true,
        message: "Daily emails triggered successfully",
        result: result.data,
        timestamp: new Date().toISOString()
      }),
      {
        status: 200,
        headers: { "Content-Type": "application/json", ...corsHeaders },
      }
    );

  } catch (error: any) {
    console.error('[TRIGGER-EMAILS] Error:', error);
    return new Response(
      JSON.stringify({
        success: false,
        error: error.message,
        timestamp: new Date().toISOString()
      }),
      {
        status: 500,
        headers: { "Content-Type": "application/json", ...corsHeaders },
      }
    );
  }
};

serve(handler);